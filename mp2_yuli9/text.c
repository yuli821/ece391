/*
 * text_to_img
 *   DESCRIPTION: Convert the string message to image format, each pixel store a byte (index to the palatte)
 *   INPUTS: text -- the string message we want to convert, text_buff -- the pointer to build buffer for status bar
 *   OUTPUTS: none
 *   RETURN VALUE: 0 on success, -1 on failure
 *   SIDE EFFECTS: fill the build buffer of status bar with pixels after we convert the message to image
 */   
int text_to_img(char* text, unsigned char* text_buff) {        //text_buff is the build buffer, declared in modex.c
    if(text == NULL) {
        return -1;
    }
    //convert text to image, store in the buffer_text
    char temp[18 * IMAGE_X_DIM];                               //18 is the height of status bar, the height of font is 16 pixel, with one pixel above and one pixel below
    memset(temp, 0x02, 18 * IMAGE_X_DIM);                      //set all the pixel to the background color at first
    int i = 0;
    int start_off, off1,width;
    int count = strlen(text);
    width = count * 8;                                         //the width of the font is 8 pixels, width is number of characters * 8
    start_off = (IMAGE_X_DIM - width)/2 + IMAGE_X_DIM;         //calculate where to wrtie the message, we want it to be centered, we also add the offset of one line of pixel above
    while(text[i] != '\0') {
        int j = 0;
        off1 = start_off + 8*i;                                //off1 is the offset to the start of one line within a character, it points to the starting pixel of a character
        for( ;j < 16; j++) {                                   //when we write a character
            int k = 0;
            char b = font_data[(int)text[i]][j];                    //get the bit mask for a line of pixel
            for( ;k < 8; k++) {
                if(b & (1<<(7-k))) {
                    temp[off1 + k] = 0x20;                     //the character is drawn in red
                }else{
                    temp[off1 + k] = 0x02;                     //the bkg color is blue
                }
            }
            off1 += IMAGE_X_DIM;                               //update off1 by the number of pixel in a line since we want to write to a new line of pixel
        }
        i++;
    }                                                          //write to the build buffer in the order of 4 planes
    int k = 0;                                                 //iteration variable
    int arr_off = 3;                                           //3 is plane offset, wrtie to plane 3 first
    unsigned char* buf = text_buff;
    for (; k < 18 * IMAGE_X_DIM; k++) {
        buf[arr_off * 1440] = temp[k];                         //1440 is the size of one plane
        if (--arr_off < 0) {
	        arr_off = 3;
	        buf++;
        }
    }
    return 0;
}
